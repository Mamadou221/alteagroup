// Fonction pour obtenir une traduction
function t(key, lang = 'fr') {
  if (!key) return '';
  
  const parts = key.split('.');
  let current = translations[lang] || translations.fr;
  
  for (const part of parts) {
    if (current === null || current === undefined) {
      console.warn(`Translation key missing: ${key} for language: ${lang}`);
      return key;
    }
    current = current[part];
  }
  
  if (current === null || current === undefined) {
    console.warn(`Translation key missing: ${key} for language: ${lang}`);
    return key;
  }
  
  return current;
}

// Fonction pour changer la langue
function setLanguage(lang) {
  if (lang !== 'fr' && lang !== 'en') return;
  
  try {
    localStorage.setItem('altea-lang', lang);
    document.documentElement.lang = lang;
    
    // Mettre à jour tous les éléments avec data-translate
    document.querySelectorAll('[data-translate]').forEach(el => {
      const key = el.getAttribute('data-translate');
      el.textContent = t(key, lang);
    });
    
    // Mettre à jour les placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
      const key = el.getAttribute('data-translate-placeholder');
      el.placeholder = t(key, lang);
    });
    
    // Mettre à jour les attributs alt
    document.querySelectorAll('[data-translate-alt]').forEach(el => {
      const key = el.getAttribute('data-translate-alt');
      el.alt = t(key, lang);
    });
    
    // Mettre à jour les attributs title
    document.querySelectorAll('[data-translate-title]').forEach(el => {
      const key = el.getAttribute('data-translate-title');
      el.title = t(key, lang);
    });
    
    // Mettre à jour les attributs data-translate avec indices (pour tableaux)
    document.querySelectorAll('[data-translate]').forEach(el => {
      const key = el.getAttribute('data-translate');
      if (key.includes('.')) {
        const parts = key.split('.');
        const lastPart = parts[parts.length - 1];
        // Si c'est un index numérique, c'est probablement un tableau
        if (!isNaN(lastPart)) {
          const arrayKey = parts.slice(0, -1).join('.');
          const index = parseInt(lastPart);
          const array = t(arrayKey, lang);
          if (Array.isArray(array) && array[index]) {
            el.textContent = array[index];
          }
        } else {
          el.textContent = t(key, lang);
        }
      } else {
        el.textContent = t(key, lang);
      }
    });
    
    // Mettre à jour les boutons de langue
    document.querySelectorAll('.lang-btn').forEach(btn => {
      if (btn.dataset.lang === lang) {
        btn.classList.add('active');
      } else {
        btn.classList.remove('active');
      }
    });
  } catch (e) {
    console.error('Error setting language:', e);
  }
}

// Initialiser la langue au chargement
function initLanguage() {
  try {
    const saved = localStorage.getItem('altea-lang');
    const lang = (saved === 'fr' || saved === 'en') ? saved : 'fr';
    setLanguage(lang);
  } catch (e) {
    console.error('Error initializing language:', e);
    setLanguage('fr');
  }
}

